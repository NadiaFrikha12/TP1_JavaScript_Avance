/*
Fonctions fléchées
Réécrire une fonction classique function somme(a, b) en fonction fléchée.
Ajouter un return implicite.
*/


// Fonction classique
function somme(a, b) {
  return a + b;
}

// Fonction fléchée
const somme = (a, b) => {
  return a + b;
};

//return implicite
const somme = (a, b) => a + b;

